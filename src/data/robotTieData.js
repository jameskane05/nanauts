// Get tie data by type
export function getTieData(tieType) {
  switch (tieType) {
    case TIE_TYPES.NECKTIE:
      return NECKTIE;
    case TIE_TYPES.BOWTIE:
      return BOWTIE;
    case TIE_TYPES.CUMBERBUND:
      return CUMBERBUND;
    default:
      return BOWTIE; // Default to bowtie
  }
}

// Get grid dimensions for a tie type
export function getTieDimensions(tieType) {
  const data = getTieData(tieType);
  const height = data.STILL.length;
  const width = data.STILL[0].length;
  return { width, height };
}
